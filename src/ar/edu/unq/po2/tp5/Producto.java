package ar.edu.unq.po2.tp5;

public abstract class Producto {

		//Variables de instancia
		private double precio;
	
		//Constructor
		public Producto(double precio) {
			this.precio = precio;
		}
		
		//Metodos
		
		
		//Getters and setters
		public double getPrecio() {
			return this.precio;
		}
}	
